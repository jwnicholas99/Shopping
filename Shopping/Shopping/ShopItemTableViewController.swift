    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: "ShopTableViewCell", for: indexPath) as? ShopTableViewCell else {
            fatalError("Cannot initialize as not instance of ShopTableViewCell")
        }

        let shopItem = shopItems[indexPath.row]
        // Configure the cell...
        cell.itemName.text = shopItem.name
        cell.itemPrice.text = shopItem.price
        cell.photo.image = shopItem.photo

        return cell
    }
    

